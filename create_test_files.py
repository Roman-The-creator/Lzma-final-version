"""
Генератор тестовых файлов на русском языке для LZMA архиватора
"""


def create_test_files():
    print("Создание тестовых файлов для LZMA архиватора...")
    
    # Файл 1: Короткий текст с повторениями
    with open('file1.txt', 'w', encoding='utf-8') as f:
        f.write("Привет, мир!\n" * 100)
        f.write("Это первый тестовый файл для архиватора.\n")
        f.write("Он содержит повторяющийся текст для лучшего сжатия.\n" * 50)
    print("✓ file1.txt создан")
    
    # Файл 2: Средний текст с разнообразным содержимым
    with open('file2.txt', 'w', encoding='utf-8') as f:
        f.write("Быстрая коричневая лиса прыгает через ленивую собаку.\n" * 80)
        f.write("Это второй тестовый файл для LZMA архиватора.\n")
        f.write("LZMA - это алгоритм сжатия данных на основе LZ77 и Range Encoder.\n" * 30)
    print(" file2.txt создан")
    
    # Файл 3: Текст с русскими словами
    with open('file3.txt', 'w', encoding='utf-8') as f:
        f.write("Это третий тестовый файл для архиватора.\n")
        f.write("Архиватор сжимает файлы используя алгоритм LZMA.\n" * 40)
    print(" file3.txt создан")
    
    print("\nТестовые файлы успешно созданы!")



if __name__ == '__main__':
    create_test_files()
